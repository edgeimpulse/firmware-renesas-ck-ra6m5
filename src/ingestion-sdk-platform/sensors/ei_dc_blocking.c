#include "ei_dc_blocking.h"

static float ym1 = 0.0;
static float xm1 = 0.0;

/**
 *
 * @param x
 * @return
 */
int32_t dc_block_filter(int32_t x)
{
    int32_t y;

    ym1 = (float)(x - xm1 + (0.995 * ym1));

    xm1 = x;
    y = ym1;

    return y;
}
